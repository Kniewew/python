import pandas as pd

# Загрузка данных из файла
df = pd.read_excel('1s.xlsx')

# Переименование колонок для удобства
df.columns = ['name', 'aga', 'stepen', 'city', 'p1', 'p2', 'p3', 'p4', 'p5']

# Вычисление среднего балла для каждого студента
df['avg'] = df[['p1', 'p2', 'p3', 'p4', 'p5']].mean(axis=1)

# 1. Средний балл группы
xle = df['avg'].mean()
print(f"1. Средний балл группы: {xle:.2f}")

# 2. Студенты без троек
xle1 = df[(df[['p1', 'p2', 'p3', 'p4', 'p5']] > 3).all(axis=1)]
print("2. Студенты без троек:")
print(xle1[['name', 'avg']])

# 3. Список тех, кто не получает стипендию (stepen = 0)
xle2 = df[df['stepen'] == 0]
print("3. Студенты, не получающие стипендию:")
print(xle2[['name', 'stepen']])

# 4. Проживающие в Шумерле, у которых балл выше 4.5
xle3 = df[(df['city'] == 'Шумерля') & (df['avg'] > 4.5)]
print("4. Студенты из Шумерли с баллом > 4.5:")
print(xle3[['name', 'city', 'avg']])

# 5. Студенты не из Шумерли, имеющие хотя бы одну оценку 5
xle4 = df[(df['city'] != 'Шумерля') & ((df[['p1', 'p2', 'p3', 'p4', 'p5']] == 5).any(axis=1))]
print("5. Студенты не из Шумерли с оценкой 5:")
print(xle4[['name', 'city']])

# 6. Количество студентов, которым есть 18 лет
xle5 = (df['aga'] == 18).sum()
print(f"6. Количество студентов 18 лет: {xle5}")

# 7. Студенты старше 16 лет и не имеющие троек
xle6 = df[(df['aga'] > 16) & ((df[['p1', 'p2', 'p3', 'p4', 'p5']] > 3).all(axis=1))]
print("7. Студенты старше 16 лет без троек:")
print(xle6[['name', 'aga', 'avg']])

# 8. Студенты, проживающие в Чебоксарах
xle7 = df[df['city'] == 'Чебоксары']
print("8. Студенты из Чебоксар:")
print(xle7[['name', 'city']])

# 9. Все студенты с четным номером (по индексу, начиная с 1)
xle8 = df.iloc[1::2]  # индексы 1,3,5... (0-based)
print("9. Студенты с четным номером:")
print(xle8[['name']])