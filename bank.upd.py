# Исходные средства банка
S = 22020000  # начальная сумма в банке
# Первый кредит: 20 000 000 под 22% годовых
K1 = 20000000
p1 = 22
# Второй кредит: 2 000 000 под 20% годовых
K2 = 2000000
p2 = 20
# Третий кредит: 20 000 под 26% годовых
K3 = 20000
p3 = 26
# Месячная процентная ставка (годовая / 12 / 100)
m1 = p1 / 12 / 100
m2 = p2 / 12 / 100
m3 = p3 / 12 / 100
# Проценты, которые банк получает ежемесячно по каждому кредиту
pr1 = K1 * m1
pr2 = K2 * m2
pr3 = K3 * m3
# Сумма, которую банк получает каждый месяц от процентов
month = pr1 + pr2 + pr3
# Начальная сумма в банке
current = S
# Список для хранения сумм по месяцам
balances = []
# Расчёт суммы в банке для каждого месяца
for i in range(1, 13):
    current += month  # добавляем проценты и ежемесячное пополнение
    balances.append(current)
    print(f"Сумма в банке за {i}-й месяц: {current} рублей")
# Итоговая сумма через год
y_new = current + 10000
print(f"Сумма в банке через год: {y_new} рублей")