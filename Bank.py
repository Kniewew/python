# Исходные средства банка
S = 22020000  # начальная сумма в банке
# Первый кредит: 20 000 000 под 22% годовых
K1 = 20000000
p1 = 22
# Второй кредит: 2 000 000 под 20% годовых
K2 = 2000000
p2 = 20
# Третий кредит: 20 000 под 26% годовых
K3 = 20000
p3 = 26
# Месячная процентная ставка (годовая / 12 / 100)
m1 = p1 / 12 / 100
m2 = p2 / 12 / 100
m3 = p3 / 12 / 100
# Проценты которые банк получит через месяц по каждому кредиту
pr1 = K1 * m1
pr2 = K2 * m2
pr3 = K3 * m3
# Общая сумма в банке через месяц
# начальные средства минус выданные кредиты + возврат кредитов + проценты
# начальные средства + проценты
S_new = S + pr1 + pr2 + pr3 + 10000
print(f"Сумма в банке через месяц: {S_new:.2f} рублей")